 public void load_table(){

           DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Id");
        model.addColumn("Nombre");
        model.addColumn("Localizacion");
        model.addColumn("Direccion IP");
        model.addColumn("Direccion MAC");
        Connection con= null;

                try{
        String url = Database.conection_string;
            con = DriverManager.getConnection(url);

                        ///////// CREAR TABLA SI NO HA SIDO CREADA
            String sql_tabla = "CREATE TABLE IF NOT EXISTS contacto ("
                    + "id integer primary key,"
                    + "name text,"
                    + "location text,"
                    + "ip address text,"
                    + "mac address text,"
                    + ");";
            
            Statement stmt = con.createStatement();
            stmt.execute(sql_tabla);
            
                        String sql_select = "select * from contacto";
            Statement stmt2 = con.createStatement();
            ResultSet rs = stmt2.executeQuery(sql_select);
            
            while(rs.next()){
                        model.addRow(new Object[]{rs.getString("id"),rs.getString("name")+" "+rs.getString("location"),rs.getString("ip address"),rs.getString("mac address")});

                System.out.println("/////////////////////");
                System.out.println("Id:" + rs.getString(1));
                System.out.println("Nombre:" + rs.getString("name"));
                System.out.println("Localizacion:" + rs.getString("location"));
                System.out.println("Direccion IP:" + rs.getString("ip address"));
                 System.out.println("Direccion MAC:" + rs.getString("mac address"));
               System.out.println("/////////////////////");
            }
            
                    jTable1.setModel(model);


        }
        catch(SQLException e){
            System.out.println("Error:" + e.getMessage());
        
        }
    
    }

